namespace WEM\SmartgearBundle\Backup\Model;

use Contao\File;

class Backup
{
    /** @var File */
    protected $file;
    /** @var string */
    protected $source;

    /**
     * @return File
     */
    public function getFile()
    {
        return $this->file;
    }

    public function setFile(File $file): self
    {
        $this->file = $file;

        return $this;
    }

    /**
     * @return string
     */
    public function getSource()
    {
        return $this->source;
    }

    public function setSource(string $source): self
    {
        $this->source = $source;

        return $this;
    }
}
