namespace WEM\SmartgearBundle\Backup\Model;

use Contao\File;

class Backup
{
    protected File $file;

    protected string $source;

    public function getFile(): File
    {
        return $this->file;
    }

    public function setFile(File $file): self
    {
        $this->file = $file;

        return $this;
    }

    public function getSource(): string
    {
        return $this->source;
    }

    public function setSource(string $source): self
    {
        $this->source = $source;

        return $this;
    }
}
