namespace WEM\SmartgearBundle\Api\Nominatim\V4\Model;

class SearchResponse
{
    /** @var string */
    protected $lat;
    /** @var string */
    protected $lon;

    public function getLat(): ?string
    {
        return $this->lat;
    }

    public function setLat(?string $lat): self
    {
        $this->lat = $lat;

        return $this;
    }

    public function getLon(): ?string
    {
        return $this->lon;
    }

    public function setLon(?string $lon): self
    {
        $this->lon = $lon;

        return $this;
    }
}
